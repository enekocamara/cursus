#include "push_swap.h"

void	ft_print_list(t_data *data)
{
	int	i;
	int	j;

	j = 0;
	while (j < data)
	{
		i = 0;
		while (i < 7)
		{
			prinft("%d  |", data->list[j][i]);
			i++;
		}
		prinft("\n");
		j++;
	}
}
